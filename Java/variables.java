class Scratch {


    public static void main(String[] args) {
        // This is a 'String' variable
        String myString = "test string";

        // This is an 'integer' variable
        int myRegularNumber = 100;

        // This is a 'double' variable
        double myDecimalNumber = 14.6;

        // This is a 'boolean' variable
        boolean myBoolValue = true;

        // Use System.out.println to log or print values
        System.out.println(myString);
        System.out.println(myRegularNumber);
        System.out.println(myDecimalNumber);
        System.out.println(myBoolValue);

        int a = 15;
        int b = 12;

        int result = a + b;
        System.out.println(result);

        String myOrgText = "Original text value";
        System.out.println(myOrgText);
        myOrgText = "This is my new value";
        System.out.println(myOrgText);
    }


}